class InfoTributaria:
    def __init__(
            self,
            ambiente,
            tipoEmision,
            razonSocial,
            nombreComercial,
            ruc,
            claveAcceso,
            codDoc,
            establecimiento,
            puntoEmision,
            secuencial,
            direccionMatriz
            ):
            self.ambiente = ambiente,
            self.tipoEmision = tipoEmision,
            self.razonSocial = razonSocial,
            self.nombreComercial = nombreComercial,
            self.ruc = ruc,
            self.claveAcceso = claveAcceso,
            self.codDoc = codDoc,
            self.establecimiento = establecimiento,
            self.puntoEmision = puntoEmision,
            self.secuencial = secuencial,
            self.direccionMatriz = direccionMatriz